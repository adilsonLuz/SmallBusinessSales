    //Método listar na tabela
    public void listar() {
        FornecedoresDAO dao = new FornecedoresDAO();
        List<Fornecedores> lista = dao.listarFornecedores();
        DefaultTableModel dados = (DefaultTableModel) tabelaFornecedores.getModel();
        dados.setNumRows(0);
        for (Fornecedores fo : lista) {
            dados.addRow(new Object[]{
                fo.getId(),
                fo.getNome(),
                fo.getCnpj(),                
                fo.getEmail(),
                fo.getTelefone(),
                fo.getCelular(),
                fo.getCep(),
                fo.getEndereco(),
                fo.getNumero(),
                fo.getComplemento(),
                fo.getBairro(),
                fo.getCidade(),
                fo.getUf()
            });
        }
    }

