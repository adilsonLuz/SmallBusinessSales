    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // Botão Excluir
        int op;
        op = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja Excluir?");

        if (op == 0) {            
            try {
                Fornecedores obj = new Fornecedores();

                obj.setId(Integer.parseInt(txtCod.getText()));

                FornecedoresDAO dao = new FornecedoresDAO();
                dao.excluirFornecedores(obj);

                new Helpers().limpaTela(panelDados);

            } catch (Exception e) {
                JOptionPane.showConfirmDialog(null, "Ops.. houve o erro " + e);
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btConsNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsNomeActionPerformed
        // Botão pesquisar por nome
        String nome = "%" + txtNomeCons.getText() + "%";

        FornecedoresDAO dao = new FornecedoresDAO();
        List<Fornecedores> lista = dao.buscarFornecedoresPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tabelaFornecedores.getModel();
        dados.setNumRows(0);
        for (Fornecedores fo : lista) {
            dados.addRow(new Object[]{
                fo.getId(),
                fo.getNome(),
                fo.getCnpj(),                
                fo.getEmail(),
                fo.getTelefone(),
                fo.getCelular(),
                fo.getCep(),
                fo.getEndereco(),
                fo.getNumero(),
                fo.getComplemento(),
                fo.getBairro(),
                fo.getCidade(),
                fo.getUf()
            });
        }
    }//GEN-LAST:event_btConsNomeActionPerformed

