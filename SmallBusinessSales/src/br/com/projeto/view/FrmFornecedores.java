    private void btPesqNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesqNomeActionPerformed
        //Botão que busca um cliente por nome

        String nome = txtNome.getText();
        Fornecedores obj = new Fornecedores();
        FornecedoresDAO dao = new FornecedoresDAO();

        obj = dao.consultarFornecedoresPorNome(nome);

        if (obj.getNome() != null) {
            //Exibir os dados do obj nos campos de texto
            txtCod.setText(String.valueOf(obj.getId()));
            txtNome.setText(obj.getNome());
            ftxtCnpj.setText(obj.getCnpj());
            txtEmail.setText(obj.getEmail());
            ftxtTel.setText(obj.getTelefone());
            ftxtCel.setText(obj.getCelular());
            ftxtCep.setText(obj.getCep());
            txtEnd.setText(obj.getEndereco());
            txtNum.setText(String.valueOf(obj.getNumero()));
            txtCompl.setText(obj.getComplemento());
            txtBairro.setText(obj.getBairro());
            txtCidade.setText(obj.getCidade());
            cbUf.setSelectedItem(obj.getUf());
        } else {
            JOptionPane.showMessageDialog(null, "Fornecedor não Encontrado!!!");
        }
    }//GEN-LAST:event_btPesqNomeActionPerformed

