    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // Bot√£o calcular total de venda por data
        try {
            //Receber a data
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            String data = formato.format(txtData.getDate());
            LocalDate data_venda = LocalDate.parse(data);
            
            double total_venda;
            VendasDAO dao = new VendasDAO();
            total_venda = dao.retornaTotalVendaPorData(data_venda);
            
            txtTotalVenda.setText(String.valueOf(total_venda).format("%.2f", total_venda));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Informe uma data corretamente" + e);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

